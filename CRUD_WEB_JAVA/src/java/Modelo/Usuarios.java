package Modelo;

public class Usuarios {
    int id;
    String nombre;
    int edad;
    String celular;

    public Usuarios() {
    }

    public Usuarios(String nombre, int edad, String celular) {
        this.nombre = nombre;
        this.edad = edad;
        this.celular = celular;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getCelular() {
        return celular;
    }

    public void setCelular(String celular) {
        this.celular = celular;
    }
}
